/**
 * Converts the given value to a number.
 * In case of exception,it returns the default value.
 * @param {*} value the value to convert to a number.
 * @param {*} defaultValue the value to return in case of error.
 * @returns the value as a number,or the default value.
 */
export const getNumber = (value, defaultValue = undefined) => {
    try {
        return Number(value);
    }
    catch {
        return defaultValue;
    }
}

/**
 * Converts the given object to an enumeration.
 * 
 * The input object shall be a map of names to unique values.
 * 
 * The input object is transformed like this:
 * 
 *  1. the object is frozen.
 *  2. the object get a `getName(value)` method that returns the name from a value.
 *  3. the object gets a `getNames()` method for retrieving an array of names.
 *  4. the object gets a `hasName(name)` method for checking if a name is valid.
 *  4. the object gets a `hasValue(value)` method for checking if a value is valid.
 * 
 * @param {*} defs the enumeration definition.
 * 
 * @returns a new object that is transformed as described above.
 */
export const makeEnum = (defs) => {
    const enumObj = {...defs};

    enumObj.getName = (value) => {
        for(const key in enumObj) {
            if (enumObj[key] === value) {
                return key;
            }
        }
        return null;
    };

    enumObj.getNames = () => {
        const result = [];
        for(const key in enumObj) {
            result.push(key);
        }
        return result;
    }

    enumObj.hasName = (name) => {
        return Boolean(enumObj[name]);
    }

    enumObj.hasValue = (value) => {
        for(const key in enumObj) {
            if (enumObj[key] === value) {
                return true;
            }
        }
        return false;
    }

    return Object.freeze(enumObj);
}

/**
 * Returns the decimal separator for the platform.
 * @returns the current decimal separator.
 */
export const getDecimalSeparator = () => {
    const numberWithDecimalSeparator = 1.1; 
    const parts = new Intl.NumberFormat().formatToParts(numberWithDecimalSeparator);
    const decimalPart = parts.find(part => part.type === 'decimal');  
    return decimalPart ? decimalPart.value : '.';
}

/**
 * Counts the number of decimals of the given value.
 * @param {*} value the value to get the decimals of.
 * @returns the number of decimals; if the value has no decimals, or it is not a number, then 0.
 */
export const countDecimals = (value) => {
    if (isNumber(value)) {
        const decimalSeparator = getDecimalSeparator();
        const parts = value.toString().split(decimalSeparator);
        return parts.length === 2 ? parts[1].length : 0;
    }
    return 0;
}

